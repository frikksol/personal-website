  <p class="text-lg">
    During the end of my studies and the first half year after graduating, I was
    a part of creating TrashTag. This was an initiative to leverage the use of
    "cheap" consumer sub sea ROV's, and outfitting these with sensors to log
    location and depth of marine trash.
    <br /><br />
    The reasoning for the project was that marine trash is a huge problem, and using
    divers to search for, and retrieve, said trash is a very expensive operation.
    Consumer ROV's equipped with sensors and logging systems, is a much cheaper solution,
    and based on the data, divers can to much more effective dives to retrieve the
    trash. In addition, these devices can be used as a learning platform for kids,
    students etc, where they can learn about how amazing the ocean is, while making
    a change at the same time.
    <br /><br />
    The project was funded by the Norwegian Environment Agency (Milj√∏direktoratet).
    We were able to get prototypes up and running, but we were never able to finish
    the project fully since we later got "proper" jobs and needed to prioritize these.
    We however passed the project on to Norwegian University of Life Sciences (NMBU),
    which continued working for a while. Now I am no longer sure.
